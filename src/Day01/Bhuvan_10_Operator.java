package Day01;

import java.util.Scanner;
public class Bhuvan_10_Operator {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        // Arithmatic Operator
        int f1=2;
        int f2=3;
        System.out.println(f1*f2);
        System.out.println(f1+f2);
        // Assignment Operator
        int b=9;
        b*=3;
        System.out.println(b);
        // Comparison Operator
        System.out.println(64>6);
        // Logical Operator
        System.out.println(64>5 && 5<3);
        // Bitwise Operator
        System.out.println(2&3);
    }
}
